package game;

public class CactusSpikeProjectile {
    double pixelX;
    double pixelY;
    int directionX;
    int directionY;

    CactusSpikeProjectile(double pixelX, double pixelY, int directionX, int directionY) {
        this.pixelX = pixelX;
        this.pixelY = pixelY;
        this.directionX = directionX;
        this.directionY = directionY;
    }
}
